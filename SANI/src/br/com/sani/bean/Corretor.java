package br.com.sani.bean;

public class Corretor {
	
	private int codCorretor;
	private String cep;
	private String numeroEndereco;
	private String complementoEndereco;
	private String nome;
	private String cpf;
	private String rg;
	private String numeroCreci;
	private String sexo;
	private String dataNascimento;
	private String estadoCivil;
	
	//Getters & Setters
	
	public int getCodCorretor() {
		return codCorretor;
	}
	public void setCodCorretor(int codCorretor) {
		this.codCorretor = codCorretor;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getNumeroEndereco() {
		return numeroEndereco;
	}
	public void setNumeroEndereco(String numeroEndereco) {
		this.numeroEndereco = numeroEndereco;
	}
	public String getComplementoEndereco() {
		return complementoEndereco;
	}
	public void setComplementoEndereco(String complementoEndereco) {
		this.complementoEndereco = complementoEndereco;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getRg() {
		return rg;
	}
	public void setRg(String rg) {
		this.rg = rg;
	}
	public String getNumeroCreci() {
		return numeroCreci;
	}
	public void setNumeroCreci(String numeroCreci) {
		this.numeroCreci = numeroCreci;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getDataNascimento() {
		return dataNascimento;
	}
	public void setDataNascimento(String dataNascimento) {
		this.dataNascimento = dataNascimento;
	}
	public String getEstadoCivil() {
		return estadoCivil;
	}
	public void setEstadoCivil(String estadoCivil) {
		this.estadoCivil = estadoCivil;
	}	
	
}
