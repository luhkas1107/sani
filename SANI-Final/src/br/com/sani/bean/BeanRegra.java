package br.com.sani.bean;

public class BeanRegra {
	
	private int codRegra;
	private String descRegra;
	
	//Getters & Setters
	
	public int getCodRegra() {
		return codRegra;
	}
	public void setCodRegra(int codRegra) {
		this.codRegra = codRegra;
	}
	public String getDescRegra() {
		return descRegra;
	}
	public void setDescRegra(String descRegra) {
		this.descRegra = descRegra;
	}

}
